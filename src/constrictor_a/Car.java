package constrictor_a;

public class Car {
	
	String make;
	String model;
	int year;
	
	public Car() {
		this.make = "Unknown";
		this.model = " Unknown";
		this.year = 0;
	}
	
	public Car(String make, String model, int year) {
		this.make = make;
		this.model = model;
		this.year = year;
	}
	
	

}
